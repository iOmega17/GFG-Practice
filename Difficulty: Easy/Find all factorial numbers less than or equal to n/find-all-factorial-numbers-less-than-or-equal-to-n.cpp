class Solution {
  public:
    vector<long long> factorialNumbers(long long n) {
        long long fact = 1;
        long long i = 1;
        vector<long long> a;
        while(fact<=n)
        {
            fact *= i;
            if(fact>n)
                break;
            i++;
            a.push_back(fact);
            
        }
        return a;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        long long N;
        cin >> N;
        Solution ob;
        vector<long long> ans = ob.factorialNumbers(N);
        for (auto num : ans) {
            cout << num << " ";
        }
        cout << endl;
    }
    return 0;
}
